-- 3. STUDENTS TABLE (Stores detailed profile data)
CREATE TABLE public.students (
  id UUID PRIMARY KEY REFERENCES auth.users ON DELETE CASCADE,
  enrollment_id TEXT UNIQUE NOT NULL,
  name TEXT NOT NULL,
  father_name TEXT,
  phone TEXT,
  parent_name TEXT,
  parent_phone TEXT,
  email TEXT,
  address TEXT,
  batch_name TEXT NOT NULL, -- e.g. "MPPSC"
  course TEXT,
  valid_from TEXT,
  valid_till TEXT,
  dob TEXT,
  fee_amount NUMERIC NOT NULL DEFAULT 2500,
  fee_status TEXT DEFAULT 'unpaid' CHECK (fee_status IN ('paid', 'unpaid', 'overdue')),
  next_due_date DATE DEFAULT (CURRENT_DATE + INTERVAL '1 month'),
  admission_date DATE DEFAULT CURRENT_DATE,
  photo_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.students ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow public read of students" ON public.students FOR SELECT USING (true);
CREATE POLICY "Allow users to update own student record" ON public.students FOR UPDATE USING (auth.uid() = id);
CREATE POLICY "Allow admin to manage students" ON public.students FOR ALL USING (
  EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin')
);

-- 4. ATTENDANCE TABLE
CREATE TABLE public.attendance (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  student_id UUID REFERENCES public.students(id) ON DELETE CASCADE NOT NULL,
  date DATE DEFAULT CURRENT_DATE NOT NULL,
  status TEXT NOT NULL CHECK (status IN ('present', 'absent', 'holiday')),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE (student_id, date)
);

ALTER TABLE public.attendance ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow users to read own attendance" ON public.attendance FOR SELECT USING (
  auth.uid() = student_id OR EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin')
);
CREATE POLICY "Allow admin to manage attendance" ON public.attendance FOR ALL USING (
  EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin')
);

-- 5. PAYMENTS TABLE (Fee history)
CREATE TABLE public.payments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  student_id UUID REFERENCES public.students(id) ON DELETE CASCADE NOT NULL,
  month TEXT NOT NULL,
  amount NUMERIC NOT NULL,
  status TEXT DEFAULT 'paid' CHECK (status IN ('paid', 'unpaid', 'overdue')),
  paid_date TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
  receipt_no TEXT UNIQUE NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.payments ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow users to read own payments" ON public.payments FOR SELECT USING (
  auth.uid() = student_id OR EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin')
);
CREATE POLICY "Allow admin to manage payments" ON public.payments FOR ALL USING (
  EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'admin')
);

-- 6. COMMUNITY POSTS TABLE
CREATE TABLE public.community_posts (
