-- 4. HANDLE NEW USER TRIGGER
DROP FUNCTION IF EXISTS public.handle_new_user() CASCADE;
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
DECLARE
  existing_student_id UUID;
  user_role TEXT;
BEGIN
  user_role := new.raw_user_meta_data->>'role';

  INSERT INTO public.profiles (id, name, email, role)
  VALUES (
    new.id,
    COALESCE(new.raw_user_meta_data->>'name', 'Student User'),
    new.email,
    user_role
  )
  ON CONFLICT (id) DO UPDATE
  SET name = EXCLUDED.name,
      email = EXCLUDED.email,
      role = COALESCE(profiles.role, EXCLUDED.role);

  IF user_role IS NULL OR user_role = 'student' THEN
    SELECT id INTO existing_student_id FROM public.students WHERE email = new.email LIMIT 1;
    IF existing_student_id IS NOT NULL THEN
      UPDATE public.students SET user_id = new.id WHERE id = existing_student_id;
      UPDATE public.profiles SET role = 'student' WHERE id = new.id;
    END IF;
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();
